    private void loadActiveProducts() {
        try {
        // Obtener productos disponibles del controlador
        List<Product> availableProducts = productController.getAvailableProducts();
        
        // Obtener el modelo de la tabla
        DefaultTableModel model = (DefaultTableModel) tabla_Bebidas.getModel();
        model.setRowCount(0); // Limpiar la tabla
        
        // Filtrar solo productos de categoría 'C' y llenar la tabla
        for (Product product : availableProducts) {
            if (product.getCategoria() == 'B') {  // Solo incluir si la categoría es 'C'
                Object[] row = {
                    product.getId(),
                    product.getNombre(),
                    product.getCategoria(),  // Mostrará 'C' en la tabla
                    product.getPrecio(),
                    product.getStock()
                };
                model.addRow(row);
            }
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, 
            "Error al cargar productos: " + e.getMessage(), 
            "Error", JOptionPane.ERROR_MESSAGE);
    }
    }
