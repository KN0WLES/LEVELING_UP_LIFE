    private void boton_comprarComidasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_comprarComidasActionPerformed
        int selectedRow = tabla_Comidas.getSelectedRow();
        if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, 
            "Por favor, seleccione un producto para comprar", 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
        return;
        }

    // Obtener datos del producto
    String productId = tabla_Comidas.getValueAt(selectedRow, 0).toString();
    String productName = tabla_Comidas.getValueAt(selectedRow, 1).toString();
    double precio = Double.parseDouble(tabla_Comidas.getValueAt(selectedRow, 3).toString());
    int stock = Integer.parseInt(tabla_Comidas.getValueAt(selectedRow, 4).toString());

    // Panel de compra
    JPanel panel = new JPanel(new GridLayout(3, 2));
    JSpinner cantidadSpinner = new JSpinner(new SpinnerNumberModel(1, 1, stock, 1));
    
    panel.add(new JLabel("Producto:"));
    panel.add(new JLabel(productName));
    panel.add(new JLabel("Precio unitario:"));
    panel.add(new JLabel("$" + precio));
    panel.add(new JLabel("Cantidad (M√°x: " + stock + "):"));
    panel.add(cantidadSpinner);
    
    int result = JOptionPane.showConfirmDialog(
        this, 
        panel, 
        "Confirmar Compra", 
        JOptionPane.OK_CANCEL_OPTION, 
        JOptionPane.PLAIN_MESSAGE);
    
    if (result == JOptionPane.OK_OPTION) {
        try {
            int cantidad = (int) cantidadSpinner.getValue();
            double total = precio * cantidad;
            
            // Actualizar stock (asumiendo que productController tiene este m√©todo)
            productController.updateStock(productId, -cantidad);
            
            JOptionPane.showMessageDialog(this, 
                "Compra realizada!\n" +
                "Producto: " + productName + "\n" +
                "Cantidad: " + cantidad + "\n" +
                "Total: $" + total,
                "√âxito", 
                JOptionPane.INFORMATION_MESSAGE);
            
            // Actualizar tabla
            loadActiveProducts();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error al comprar: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_boton_comprarComidasActionPerformed

