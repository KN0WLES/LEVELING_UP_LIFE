    private void loadActiveProducts() {
        try {
        // Obtener productos disponibles del controlador
        List<Product> availableProducts = productController.getAvailableProducts();
        
        // Obtener el modelo de la tabla
        DefaultTableModel model = (DefaultTableModel) tabla_Pdisponibles.getModel();
        model.setRowCount(0); // Limpiar la tabla
        
        // Llenar la tabla con los productos
        for (Product product : availableProducts) {
            Object[] row = {
                product.getId(),
                product.getNombre(),
                product.getCategoria(),
                product.getPrecio(),
                product.getStock()
            };
            model.addRow(row);
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    }
